package br.com.erickfreire.numerosdeumadez2;

/**
 * Programa em Java que mostra os valores de 1 a 10 com laço for
 * @author Erick Freire
 * @version 1 - Criado em 14-04-2021 as 17:56
 */

public class NumerosDeUmADez {
	public static void main(String[] args) {
		for(int i = 1; i <= 10; i++) {
			System.out.printf("%d ", i);
		}
		
		System.out.println();
	}

}
